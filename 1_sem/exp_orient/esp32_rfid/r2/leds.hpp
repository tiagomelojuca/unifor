// ----------------------------------------------------------------------------------------------------------
// LED CONTROL
// ----------------------------------------------------------------------------------------------------------

#define DELAY_IN_MS 200
#define LED_PIN      15

// ----------------------------------------------------------------------------------------------------------

inline void leds_setup()
{
  pinMode(LED_PIN, OUTPUT);
}

// ----------------------------------------------------------------------------------------------------------

inline void leds_blink()
{
  digitalWrite(LED_PIN, HIGH);
  delay(DELAY_IN_MS);
  digitalWrite(LED_PIN, LOW);
}

