// ----------------------------------------------------------------------------------------------------------
// LED CONTROL
// ----------------------------------------------------------------------------------------------------------

#define DELAY_IN_MS 200

enum LED_PINS
{
  LED_RED   = 12,
  LED_GREEN = 13
};

// ----------------------------------------------------------------------------------------------------------

inline void leds_setup()
{
  pinMode(LED_RED,   OUTPUT);
  pinMode(LED_GREEN, OUTPUT);
}

// ----------------------------------------------------------------------------------------------------------

inline void leds_blink(int led)
{
  digitalWrite(led, HIGH);
  delay(DELAY_IN_MS);
  digitalWrite(led, LOW);
}

// ----------------------------------------------------------------------------------------------------------

void blink_green_led()
{
  leds_blink(LED_GREEN);
}

// ----------------------------------------------------------------------------------------------------------

void blink_red_led()
{
  leds_blink(LED_RED);
}

